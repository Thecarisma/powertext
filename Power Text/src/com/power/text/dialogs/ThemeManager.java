    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!jTextField57.getText().equals("")) {
            FileOutputStream Stream = null;
            try {
                themeName = jTextField57.getText() ;
                if (themeName.endsWith(".xml")) { } else { themeName = themeName + ".xml" ; }
                Stream = new FileOutputStream(getMyThemeFolder()+themeName);
                createTheme(Stream); 
            } catch (FileNotFoundException ex) {
                Logger.getLogger(ThemeManager.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    Stream.close(); 
                } catch (IOException ex) {
                    Logger.getLogger(ThemeManager.class.getName()).log(Level.SEVERE, null, ex);
                }
            } if (new File(getMyThemeFolder()+themeName).exists()) {
                try {
                    Scanner scn = new Scanner(new FileInputStream(new File(getMyThemeFolder()+themeName)));
                    main.openFile(new File(getMyThemeFolder()+themeName), scn); main.addRecent(new File(getMyThemeFolder()+themeName).getAbsolutePath());
                    activateTheme(getMyThemeFolder()+themeName); 
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(ThemeManager.class.getName()).log(Level.SEVERE, null, ex);
                } 
            } else { JOptionPane.showMessageDialog(this, "Theme Creation Not Successfull ", "Theme Creation Not Successfull", JOptionPane.ERROR_MESSAGE); }
        } else { JOptionPane.showMessageDialog(this, "Specify Theme Name ", "Specify Theme Name", JOptionPane.ERROR_MESSAGE);}
    }//GEN-LAST:event_jButton3ActionPerformed

