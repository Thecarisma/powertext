    private void loadThemeFile() {
        int status = fileChooser.showOpenDialog(rootPane); 
       fileChooser.setCurrentDirectory(new File(Editor.get(jTabbedPane1.getSelectedIndex()).getParentFolder()));
        if (status == JFileChooser.APPROVE_OPTION ) {
            if (fileChooser.getSelectedFile().getName().endsWith(".xml")) {
                File files = fileChooser.getSelectedFile();
                activateTheme(files.getAbsolutePath());
            } else {
                JOptionPane.showMessageDialog(this, "Invalid Them File \".xml\" only", "Invalid Theme", JOptionPane.ERROR_MESSAGE); 
                loadThemeFile();
            }
        } else {
            System.out.println("No file selected");
        }
    }
    
    private void activateTheme(String file) {
        try {
            tm.loadTheme(new FileInputStream(file));
            tm.apply(); themePath = file ; this.dispose(); 
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ThemeManager.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
